<script>
    // Mobile menu toggle
    document.getElementById('mobileMenuButton').addEventListener('click', function() {
        const mobileMenu = document.getElementById('mobileMenu');
        mobileMenu.classList.toggle('open');
        this.innerHTML = mobileMenu.classList.contains('open')
            ? '<i class="fas fa-times"></i>'
            : '<i class="fas fa-bars"></i>';
    });

    // Scroll progress bar
    window.addEventListener('scroll', function() {
        const scrollTop = window.pageYOffset;
        const docHeight = document.documentElement.scrollHeight - window.innerHeight;
        const scrollPercent = (scrollTop / docHeight) * 100;
        document.getElementById('scrollProgress').style.width = scrollPercent + '%';
    });

    // Set current year in footer
    document.getElementById('currentYear').textContent = new Date().getFullYear();

    // Smooth scroll for anchor links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function(e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if(targetId === '#') return;

            const targetElement = document.querySelector(targetId);
            if(targetElement) {
                window.scrollTo({
                    top: targetElement.offsetTop - 100,
                    behavior: 'smooth'
                });
            }
        });
    });

    // Newsletter form submission (frontend only)
    document.getElementById('newsletterForm')?.addEventListener('submit', function(e) {
        e.preventDefault();
        const email = this.querySelector('input[type="email"]').value;
        alert(`Thank you for subscribing with: ${email}\n(This is a frontend demo - backend integration will be added later)`);
        this.reset();
    });

    // Contact form submission
    document.getElementById('contactForm')?.addEventListener('submit', function(e) {
        e.preventDefault();

        const submitBtn = document.getElementById('contactSubmitBtn');
        const submitText = document.getElementById('submitText');
        const submitIcon = document.getElementById('submitIcon');

        // Disable button and show loading
        submitBtn.disabled = true;
        submitText.textContent = 'Sending...';
        submitIcon.className = 'fas fa-spinner fa-spin ml-2';

        // Prepare form data
        const formData = new FormData(this);

        // Send to contact handler
        fetch('contact_handler.php', {
            method: 'POST',
            body: formData
        })
        .then(response => response.json())
        .then(data => {
            // Re-enable button
            submitBtn.disabled = false;
            submitText.textContent = 'Send Message';
            submitIcon.className = 'fas fa-paper-plane ml-2';

            if (data.success) {
                // Show success toast
                showToast('Message sent successfully! We will get back to you within 24 hours.', 'success');
                this.reset();
            } else {
                // Show error toast
                showToast(data.error || 'Failed to send message. Please try again.', 'error');
            }
        })
        .catch(error => {
            // Re-enable button
            submitBtn.disabled = false;
            submitText.textContent = 'Send Message';
            submitIcon.className = 'fas fa-paper-plane ml-2';

            // Show error toast
            showToast('Network error. Please try again.', 'error');
            console.error('Contact form error:', error);
        });
    });

    // Toast notification function
    function showToast(message, type) {
        const toast = document.getElementById('toast');
        const toastMessage = document.getElementById('toastMessage');

        // Set message and type
        toastMessage.textContent = message;
        toast.className = `toast ${type}`;

        // Show toast
        setTimeout(() => {
            toast.classList.add('show');
        }, 100);

        // Hide toast after 5 seconds
        setTimeout(() => {
            toast.classList.remove('show');
        }, 5000);
    }

    // Add active class to current page link
    const urlParams = new URLSearchParams(window.location.search);
    const currentPage = urlParams.get('page') || 'home';
    const navLinks = document.querySelectorAll('.nav-link, .mobile-menu a');
    navLinks.forEach(link => {
        const href = link.getAttribute('href');
        if(href && href.includes('page=' + currentPage)) {
            link.classList.add('text-accent', 'font-bold');
            link.classList.remove('text-gray-200');

            // Add gold border for active link
            if(link.classList.contains('nav-link')) {
                link.style.setProperty('--after-width', '100%');
            }
        }
    });

    // Logo hover effect
    const logoContainer = document.querySelector('.logo-container');
    if(logoContainer) {
        logoContainer.addEventListener('mouseenter', function() {
            this.style.animation = 'shine 1.5s ease-in-out';
        });

        logoContainer.addEventListener('mouseleave', function() {
            this.style.animation = 'float 3s ease-in-out infinite';
        });
    }

    // Scroll to connect section
    window.scrollToConnect = function() {
        const connectSection = document.getElementById('connect');
        if (connectSection) {
            connectSection.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    };

    // Animation on scroll
    const observerOptions = {
        threshold: 0.1,
        rootMargin: '0px 0px -50px 0px'
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if(entry.isIntersecting) {
                entry.target.classList.add('animate-fade-in');
                entry.target.style.opacity = '1';
            }
        });
    }, observerOptions);

    // Observe all footer sections
    document.querySelectorAll('footer > div > div > div').forEach(section => {
        section.style.opacity = '0';
        observer.observe(section);
    });
